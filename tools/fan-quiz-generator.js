#!/usr/bin/env node
'use strict'

require('dotenv').config()

const path = require('path')
const fs = require('fs')
const { callClaude, extractJSON } = require('./claude')
const { calculateFan } = require('./engine/fan-calculator')

const args = process.argv.slice(2)
function getArg(flag) {
  const idx = args.indexOf(flag)
  return idx !== -1 ? args[idx + 1] : null
}

const count  = parseInt(getArg('--count') || '10')
const output = getArg('--output') || 'data/fan-quiz/fan-quiz-01.js'

async function generateFanQuizBatch(n) {
  const system = `You are an HK mahjong fan calculation expert.
Generate winning hand scenarios for fan calculation quiz questions.
No Tanyao. Return only valid JSON.
Tile notation: 1b-9b (bamboo), 1d-9d (dots), 1c-9c (characters), EW SW WW NW RD GD WD
Bonus: F1 F2 F3 F4 (flowers), S1 S2 S3 S4 (seasons)`

  const user = `Generate ${n} HK mahjong fan calculation quiz questions.

Include a variety of:
- Half Flush hands (3 fan base)
- All Pungs hands (3 fan base)
- Dragon pungs (+1 each)
- Wind pungs (+1 each)
- Self-draw vs discard wins
- Bonus tile scenarios
- Difficulty: mix of easy/medium/hard

Rules:
- Minimum 3 fan to win
- No Tanyao
- Each hand must be a valid winning hand (4 sets + 1 pair, or 7 pairs)
- Winning hand = 14 tiles total (including the winning tile)

Return JSON array:
[
  {
    "id": "fq-001",
    "hand": ["1b","2b","3b","4b","5b","6b","7b","8b","9b","WD","WD","WD","3c","3c"],
    "conditions": {
      "seat": "South",
      "round": "East",
      "winBy": "self-draw",
      "bonus": []
    },
    "answer": {
      "total": 5,
      "sources": [
        {"item":"Half Flush (混一色)","fan":3},
        {"item":"White Dragon pung","fan":1},
        {"item":"Self-draw (自摸)","fan":1}
      ]
    },
    "commonMistake": "Forgetting that self-draw adds +1 fan",
    "difficulty": "easy"
  }
]`

  return extractJSON(await callClaude(system, user, 4000))
}

async function main() {
  console.log(`Generating ${count} fan quiz questions...`)

  const raw = await generateFanQuizBatch(count)
  const questions = Array.isArray(raw) ? raw : raw.questions || []

  // Validate each answer with calculateFan()
  const validated = []
  const rejected = []

  for (const q of questions) {
    if (!q.hand || !q.conditions || !q.answer) {
      rejected.push({ q, reason: 'Missing required fields' })
      continue
    }

    const result = calculateFan(q.hand, {
      seat: q.conditions.seat,
      roundWind: q.conditions.round,
      winBy: q.conditions.winBy,
      exposed: [],
      bonus: q.conditions.bonus || [],
    })

    if (result.total !== q.answer.total) {
      rejected.push({
        q,
        reason: `Fan mismatch: Claude said ${q.answer.total}, engine calculated ${result.total}`,
        engineResult: result,
      })
      // Use engine result as the ground truth
      q.answer = result
      q._corrected = true
    }

    validated.push(q)
  }

  if (rejected.length > 0) {
    console.warn(`\nCorrected ${rejected.length} questions to use engine fan calculation:`)
    rejected.forEach(r => console.warn(' -', r.reason))
  }

  const outputPath = path.resolve(process.cwd(), output)
  fs.mkdirSync(path.dirname(outputPath), { recursive: true })

  const fileContent = `// Auto-generated by fan-quiz-generator.js
// ${new Date().toISOString()}

export const QUESTIONS = ${JSON.stringify(validated, null, 2)}
`

  fs.writeFileSync(outputPath, fileContent, 'utf8')
  console.log(`\n✓ Saved ${validated.length} questions to: ${outputPath}`)
}

main().catch(err => {
  console.error('Failed:', err.message)
  process.exit(1)
})
