#!/usr/bin/env node
'use strict'

require('dotenv').config()

const path = require('path')
const fs = require('fs')
const { callClaude, extractJSON } = require('./claude')

const args = process.argv.slice(2)
function getArg(flag) {
  const idx = args.indexOf(flag)
  return idx !== -1 ? args[idx + 1] : null
}

const count  = parseInt(getArg('--count') || '8')
const output = getArg('--output') || 'data/table-read/table-read-01.js'

async function generateTableReadBatch(n) {
  const system = `You are an HK mahjong strategy teacher.
Generate realistic table-read quiz questions based on opponent discard piles.
Return only valid JSON.`

  const user = `Generate ${n} HK mahjong table-read quiz questions.

Each question shows opponent discard piles and asks the learner to read what the opponent is building.
Questions should test:
- Identifying suit the opponent is building (discarded all of one suit = building that suit)
- Identifying honor tile danger (opponent kept honors = punging them)
- Identifying safe tiles to discard (tiles opponent has already discarded are safe)

Return JSON array:
[
  {
    "id": "tr-001",
    "scenario": "Mid-game. West has discarded: SW, 1c, 9c, 3d, 7d, 8d",
    "discardPile": {
      "West": ["SW","1c","9c","3d","7d","8d"]
    },
    "question": "What suit is West most likely building toward?",
    "options": ["Bamboo","Dots","Characters","Half Flush Bamboo"],
    "answer": "Bamboo",
    "explanation": "West has discarded all their Characters and Dots, keeping Bamboo tiles. This strongly suggests a Bamboo flush strategy.",
    "followUp": "Is it safe to discard 5b to West?",
    "followUpAnswer": "Dangerous — West is building Bamboo and may need 5b for a set.",
    "difficulty": "easy"
  }
]`

  return extractJSON(await callClaude(system, user, 4000))
}

async function main() {
  console.log(`Generating ${count} table read questions...`)

  const raw = await generateTableReadBatch(count)
  const questions = Array.isArray(raw) ? raw : raw.questions || []

  const outputPath = path.resolve(process.cwd(), output)
  fs.mkdirSync(path.dirname(outputPath), { recursive: true })

  const fileContent = `// Auto-generated by table-read-generator.js
// ${new Date().toISOString()}

export const QUESTIONS = ${JSON.stringify(questions, null, 2)}
`

  fs.writeFileSync(outputPath, fileContent, 'utf8')
  console.log(`\n✓ Saved ${questions.length} questions to: ${outputPath}`)
}

main().catch(err => {
  console.error('Failed:', err.message)
  process.exit(1)
})
