#!/usr/bin/env node
'use strict'

require('dotenv').config()

const path = require('path')
const fs = require('fs')
const { callClaude, extractJSON } = require('./claude')

const args = process.argv.slice(2)
function getArg(flag) {
  const idx = args.indexOf(flag)
  return idx !== -1 ? args[idx + 1] : null
}

const count  = parseInt(getArg('--count') || '10')
const output = getArg('--output') || 'data/pattern-quiz/pattern-quiz-01.js'

async function generatePatternQuizBatch(n) {
  const system = `You are an HK mahjong strategy teacher.
Generate mid-game hand scenarios for pattern recognition quizzes.
Return only valid JSON.`

  const user = `Generate ${n} HK mahjong pattern recognition quiz questions.

Each question shows a mid-game hand (13 tiles) and asks the learner to identify:
1. What pattern is forming (Half Flush, All Pungs, etc.)
2. What the best discard is

Include hands showing:
- Half Flush building (mostly one suit + honors)
- All Pungs building (pairs and triplets)
- Mixed/confused hands (the 3-point trap — lots of chows, 0 fan)

Return JSON array:
[
  {
    "id": "pq-001",
    "hand": ["1b","2b","3b","4b","5b","6b","7b","8b","WD","WD","3c","4c","5c"],
    "question": "What pattern is this hand building toward?",
    "options": ["Half Flush Bamboo","Full Flush Bamboo","All Pungs","No clear pattern"],
    "answer": "Half Flush Bamboo",
    "explanation": "The hand has 8 Bamboo tiles and a WD pair. Discarding the Characters would leave a pure Bamboo + WD structure qualifying for Half Flush.",
    "bestDiscard": "3c",
    "discardExplanation": "3c is part of a complete 3-4-5 Chr chow — but discarding it moves toward Half Flush. Actually: discard 4c (isolated). [adjust as needed]",
    "difficulty": "easy"
  }
]`

  return extractJSON(await callClaude(system, user, 4000))
}

async function main() {
  console.log(`Generating ${count} pattern quiz questions...`)

  const raw = await generatePatternQuizBatch(count)
  const questions = Array.isArray(raw) ? raw : raw.questions || []

  const outputPath = path.resolve(process.cwd(), output)
  fs.mkdirSync(path.dirname(outputPath), { recursive: true })

  const fileContent = `// Auto-generated by pattern-quiz-generator.js
// ${new Date().toISOString()}

export const QUESTIONS = ${JSON.stringify(questions, null, 2)}
`

  fs.writeFileSync(outputPath, fileContent, 'utf8')
  console.log(`\n✓ Saved ${questions.length} questions to: ${outputPath}`)
}

main().catch(err => {
  console.error('Failed:', err.message)
  process.exit(1)
})
